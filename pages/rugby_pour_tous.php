<h2>Rugby pour Tous – Action : "Rugby Toucher Wattrelosien"</h2>

<p><strong>Objectif</strong> : Promouvoir la pratique sportive (Rugby éducatif) sur le territoire de la ville de Wattrelos en utilisant le rugby comme outil de bien-être, de santé, et de mixité à destination de tous publics jeunes. Promouvoir également la pratique du rugby adapté au profit du public handicapé.</p>

<p><strong>Description</strong> : 
Le dispositif "Rugby pour Tous", mené par l'action "Rugby Toucher Wattrelosien", est un espace de découverte de l'activité rugby destiné à tous les publics. Il propose un cycle de rugby éducatif sous plusieurs formes : Rugby touché, Touch Rugby à 5, Flag Rugby, Ultimate Rugby. Il prévoit aussi des séances d’initiation au rugby adapté pour faciliter l’intégration en club.</p>

<p><strong>Bénéficiaires</strong> : Jeunes des quartiers sensibles, jeunes publics dès 3 ans.</p>

<p><strong>Moyens mobilisés</strong> :<br>
  - Espaces de jeu ou terrains aménagés<br>
  - Une salle couverte (Omni-Sport)<br>
  - Animation : 1 éducateur sportif qualifié
</p>

<p><strong>Territoire concerné</strong> : Ville de Wattrelos</p>

<p><strong>Quartiers visés</strong> :
  <br><u>Quartier Nord</u> : La Mousserie, Sapin Vert, Martinoire, Touquet
  <br><u>Quartier Centre</u> : Centre, Ballon, Crétinier, Sartel
  <br><u>Quartier Sud</u> : Beaulieu, Saint-Liévin, Baillerie
</p>

<p><strong>Période de réalisation</strong> : Du 01/09/2020 au 31/08/2021</p>
