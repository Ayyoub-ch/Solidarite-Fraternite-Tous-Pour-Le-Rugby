<h2>Rugby Solidaire – Action : "Rugby Solidaire Sans Frontière"</h2>

<p><strong>Objectif</strong> : Mener des actions caritatives, aider et soutenir les familles en difficulté, promouvoir l’insertion et l’éducation à travers le sport.</p>

<p><strong>Description</strong> : Cette action vise à collecter des vêtements (prioritairement sportifs), des jouets, et des livres au profit de jeunes en difficulté et d'autres associations caritatives. Elle permet de valoriser les jeunes et de leur donner les moyens de pratiquer le sport. Elle comprend également l'organisation d’événements sportifs à but caritatif et le parrainage d’associations par des sportifs de haut niveau.</p>

<p><strong>Bénéficiaires</strong> : Jeunes des quartiers sensibles, jeunes sportifs en difficulté, associations caritatives (Secours Populaire, Restos du Cœur, SDF, sans papiers, etc.).</p>

<p><strong>Moyens mobilisés</strong> :<br>
  - Espaces de jeu ou terrains aménagés<br>
  - Une salle couverte (Omni-Sport)<br>
  - Animation : 1 éducateur sportif qualifié
</p>

<p><strong>Territoire concerné</strong> : Ville de Wattrelos</p>

<p><strong>Quartiers visés</strong> :
  <br><u>Quartier Nord</u> : La Mousserie, Sapin Vert, Martinoire, Touquet
  <br><u>Quartier Centre</u> : Centre, Ballon, Crétinier, Sartel
  <br><u>Quartier Sud</u> : Beaulieu, Saint-Liévin, Baillerie
</p>

<p><strong>Période de réalisation</strong> : Du 01/09/2020 au 31/08/2021</p>
