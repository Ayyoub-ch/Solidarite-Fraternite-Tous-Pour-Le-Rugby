

        <h2>Comment l'association fut créer</h2>

        <p>La création de l’association Solidarité-Fraternité « Tous pour le Rugby » a été faite par <strong>Bouarfa CHETOUANI</strong> en 2019, par le
            résultat d’un double constat :</p>

        <p>- La pratique de Rugby reste insuffisante dans le patrimoine sportif local :</p>

        <p>A l’école, centres sociaux et associations de la Ville de Wattrelos, et villes voisines</p>

        <p>- La pratique des jeunes de l’activité Rugby est inexistante, de plus quand on propose une
            initiation de Rugby avec ces jeunes, ils sont contents, ils s’investissent et apprécient cette
            activité, mais c’est difficile d’intégrer un club cela explique une grande faiblesse de
            représentation des jeunes (filles et garçons) en difficulté d’intégrer un club et intégrer la vie
            sociale.</p>

        <p>Tout d’abord, la ville de Wattrelos ne dispose pas d’un club de Rugby, il y a aussi le frein
            culturel, les jeunes des quartiers prioritaires possèdent une culture de football, et non de
            Rugby, ont peu de connaissance sur ce sport, le manque d’action de développement de Rugby
            auprès du public spécifique.</p>

        <p>L’association a programmé des actions sportives de développement et d’insertion sur la ville
            de Wattrelos, et d’autres territoires, à l’aide d’un projet dans lesquels les jeunes sont au centre
            des préoccupations, car ils étaient repérés en difficulté.</p>

        <p>L’activité Rugby n’est pas une fin en soi, mais un espace sensibilisation, de découverte, qui
            doit permettre avec un accompagnement spécifique, l’épanouissement des jeunes, et leur
            construction en tant que futur citoyen, pour que cette action puisse être ce levier, il faut créer
            des partenariats avec : d’abord la ville de Wattrelos, établissements scolaires, centre sociaux,
            centre spécialisé, autres associations de la ville de Wattrelos, des Clubs, rencontrer d’autres
            acteurs d’insertion par le sport, et des partenariats privés.</p>

        <p>Pour développer ses actions, l’association a mis en place des projets dans la ville de
            Wattrelos, dans un premier temps, suite à une rencontre avec d’autres associations, pour
            ensuite développer des actions sportifs (proposer des cycles de Rugby éducatif et
            accompagnement des jeunes dans leurs parcours de vie).</p>

        <p>Depuis la création de l’association, des actions éducatives de Rugby est menées avec la
            structure d’Acti’jeunes à Wattrelos.</p>

        



