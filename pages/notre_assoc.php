			<h4>La solidarité:</h4> 

			<p>L'esprit d'équipe est une composante essentielle de l'esprit sportif.
			
			La recherche des performances individuelles doit parfois s'effacer devant l'intérêt collectif.
			
			La générosité, l'abnégation, la compréhension mutuelle, l'humilité même sont aussi louables
			
			que  la volonté de vaincre. Le sport est aussi école de solidarité.</p>

			<h4>La fraternité:</h4>

			<p>Le sport unit les personnes dans l'effort, quelles que soient leurs origines, leurs niveaux sociaux, leurs opinions ou leurs croyances, il est école de tolérance, de solidarité et facteur de rapprochement humain. Il est aussi, dans un monde ou les inégalités sont de plus en plus criantes, un formidable outil d'émancipation et d'intégration sociale.

